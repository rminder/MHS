	[RowId]
   ,[Id]
   ,[Name]
   ,[ProductCode]
   ,[Description]
   ,IIF([IsActive] = 255, 1, [IsActive]) AS IsActive
   ,[CreatedDate]
   ,[CreatedById]
   ,[LastModifiedDate]
   ,[LastModifiedById]
   ,[SystemModstamp]
   ,[Family]
   ,[RecordTypeId]
   ,[ExternalDataSourceId]
   ,[ExternalId]
   ,[DisplayUrl]
   ,[QuantityUnitOfMeasure]
   ,IIF([IsDeleted] = 255, 1, [IsDeleted]) AS [IsDeleted]
   ,[LastViewedDate]
   ,[LastReferencedDate]
   ,IIF([ServiceMaxEnableSerializedTracking] = 255, 1, [ServiceMaxEnableSerializedTracking]) AS [ServiceMaxEnableSerializedTracking]
