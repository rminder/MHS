   ,IIF([HighUsageProduct] = 255, 1, [HighUsageProduct]) AS [HighUsageProduct]
   ,[Broad]
   ,[Group]
   ,[Type]
   ,[PartSerialNumber]
   ,[Model]
   ,[Make1]
   ,[UOM]
   ,[ProductId]
   ,[UnityProductExternalId]
   ,[SSVYOptimalCost]
   ,IIF([UnityIsMenuPricing] = 255, 1, [UnityIsMenuPricing]) AS [UnityIsMenuPricing]
   ,[UnityAssetType]
   ,[UnityBumperThickness]
   ,[ServiceMaxProductType]
   ,IIF([UnityIsStockable] = 255, 1, [UnityIsStockable]) AS [UnityIsStockable]
   ,[LastUpdate]
   ,[ImportDate]
   ,[ValidFrom]
   ,[ValidTo]
FROM [SF].[Product2];
GO
